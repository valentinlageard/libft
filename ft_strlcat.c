#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t i;
	size_t j;

	i = ft_strlen(dst);
	if (i > dstsize)
		return (dstsize + ft_strlen(src));
	j = 0;
	while (src[j] && i + j + 1 < dstsize)
	{
		dst[i + j] = src[j];
		j++;
	}
	if (dstsize > 0)
		dst[i + j] = 0;
	return (i + ft_strlen(src));
}
